TIC TOC — You Don't Stop!

TIC TOC is an acronym which stands for:
T – Training
I – Intuition
C – Creativity
T – Timing
O – Organization
C – Completion

The TIC is what you do to prepare for any task or goal. You must train, intuit what you'll need to do, be creative about how you do it, and then you TOC — meaning, you must have the correct timing, be organized and complete what you start. And as stated above, YOU DON'T STOP. You must always TIC and you must always TOC.

The TIC TOC You Don't Stop strategy is a skill that every instructor is expected to master and implement. It can be applied to all areas of Rick Tew's Martial Science training.

"If there is one area of the most importance. One strategy to ensure the success of any RTMS coach, it would have to be: the ability to train, use intuitive judgement, be actively creative, master their timing, stay organized and complete the task at hand." — Rick Tew
