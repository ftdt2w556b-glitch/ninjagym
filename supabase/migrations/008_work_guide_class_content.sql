DURING CLASS

• When teaching, be sure to be loud and clear.
• Make eye contact with a Smile!
• Remember to make it personal with words of encouragement.
• Be fun, but do not sacrifice control of your class.
• Physiology is key — use a lot of body movement.
• For demonstration and amusement, you may want to overdramatize the activity so it is more exciting and motivating.
• Use words and phrases such as "When we do this... then this will happen."
• Exercise lots of patience — try different ideas.
• Remember children's names and every so often tell them they are doing good.
