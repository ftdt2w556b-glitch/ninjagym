DYNAMIC STRETCHING — Leg Swings / Stretch Kicks

Step forward and say, "Fighting stance!" with high energy. Demonstrate physically, assist them into the proper stance and say, "Good Job!"

Remind the students that we are stretching, not kicking — although it is a stretch, hands should be up.

Demonstrate by swinging your leg up to the inside, the middle and to the outside.

After a few repetitions (while kicking toward the outside), turn your body 180 degrees, again facing the students.

Say, "Inside, middle, outside," counting off each full set: "1, 2, 3"

