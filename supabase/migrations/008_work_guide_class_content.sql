FINISHING CLASS — End with a Bang! (BLAST)

The last 2–10 minutes is a BLAST and will likely use the Ninja Cave to 'burn them out.'

On the mat (Dojo area) this can be:
• Running from end to end
• Run and jump
• Run to kick the heavy bag
• An obstacle course created from skills they did not do yet

This will be fun and allow them to use their energy. It also means you need to be louder and maintain more control (safety first).

In the future, the Ninja Cave will be used similar to SPARRING in a traditional school. Students (instead of fighting each other) will combat the Ninja Cave and Course with bags to hit. Coaches play attacking them as they go through the cave — decided by the student who wants more interaction from the coach.

TIMED ROTATION FORMAT:
• Timed areas of 1–2 minutes
• In each area the student is doing one activity
