STARTING CLASS — Mind-Body-Spirit (MBS) Position

Say: "Okay, line up! Mind, Body, Spirit Position!"

Be excited! (Clapping hands, etc.) and get the first student into position.

Guide the students into the MBS position: Horse stance with arms out, hands forming a triangle. Say, "Make a triangle, this is our favourite shape!" Tell the children to look at you through the triangle. Explain the MBS position and focus.

Always re-explain the stance:

HORSE STANCE:
• Feet straight
• Knees bent forward so you cannot see your toes
• Knees to the outside
• Arms naturally extended, elbows slightly bent
• Hands forming a Triangle — "Our favourite shape!"

Say: "Look at me through your triangle!!!"

One by one check the stance of each student and make the appropriate corrections. This is called a "Line Walk." Do this quickly, and compliment all students for doing well.
