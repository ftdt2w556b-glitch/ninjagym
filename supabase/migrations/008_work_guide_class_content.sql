ROLLS AND FALLS (After Stances)

After STANCES — move into teaching and practising rolls and falls.

1. Start with a verbal explanation, followed by a physical example.
2. Teach according to the level of the class. Start with forward rolls, then falls.
3. Pay attention to their techniques, giving advice and assistance where needed.
4. After some time, begin to add variety — for example: have them each stop and look around after each technique, or add a punch or kick after each technique.

MIXED LEVEL CLASSES: When you only have a few beginners in a large class of students who can already do rolls and falls — create another line for new students and teach directly, or have an assistant teach each roll or fall.
