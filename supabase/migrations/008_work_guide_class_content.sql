MENTAL FOCUS OF THE WEEK
(Extra for older kids)

The coach will now guide the class through the FOCUS of the week.

Tell everyone to look through the triangle and remind them of the focus for the class that week.

Then ask the 3 questions, followed by 3 answers:
• What can we do Mentally? (Decide TO BE)
• What can we do Physically? (Decide TO DO)
• What can we do Spiritually? (Decide to JOIN OTHERS)

Examples of focus words: Patience, energy, balance, safety, control, teamwork, discipline, focus, etc.

KEY NOTE: Use key words for association, not adding too much information, as this can cause students to lose focus and lower information retention.
