KEY REMINDERS:

Use a TIMER — 10 minutes max on each area (Stances, Rolls, Falls, Strikes, Kicks) and switch if it is feeling stagnant. There is burnout time needed.

After each technique — do an application. For example: teach Horse Stance, then do Horse Stance Side Kick. Rinse and Repeat — teach and then apply. Do NOT drag students through all 10 stances at once. Keep them MOVING and ACTIVE.

If a kick was already done as an application, do a different kick when you are onto the kick section.

Speak loud and clear — always talk to the room, not just the individual student.

Do not stand too close — at least one mat away unless fixing positions.

Never strike students with training equipment — play a game as if (like with the sword).

Stay on the mat — do not leave students on the mat alone.

NOTE: It is all about variety. Every class will combine the CORE 5. If you do a Stance and a Side Kick application, you just did a Stance and a Kick. There is always enough time to do all 5. When experienced, you will never do the same class twice.
