CLASS STRUCTURE

Core 5 skills covered every class:
• Stances
• Rolls
• Falls
• Strikes
• Kicks

Always mentally prepare or write down a class before doing it. Also review it with any assistants you may have — this will always make things run more smoothly.

When in doubt, create an obstacle course around the mat with 1–4 actions (jump, roll, stance, kick). Use a variety of these to construct a class.

Try to always play at least one game per class.

Use a "Technique of the Week."

GENERAL CLASS MANAGEMENT:
• Only do activities long enough to keep attention and have fun. Do not drag it out.
• IF attention is lost, quickly switch to another activity.
• Be sure to review, remember, and apply proper physiology and speech to keep things from getting out of hand.
• Try to make use of everything in the room creatively if you can.
• Never give kids weapons or equipment without direct supervision.
• Sometimes it is a good idea to select a "helper" from the group. Try to select a kid who is being overactive — this helps calm them down and diverts their energy to a more helpful cause.
