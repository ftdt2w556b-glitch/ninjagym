MESSAGE OF THE WEEK

Example topic: Sharing
• What is sharing?
• Who can you share with?
• How does sharing help someone else?
• What can you share?

Give kids a task to do at home that relates to the message. For example: Go home/school and share something with someone. Then ask the same questions the next class — ask about what they shared and who they shared it with.

Encourage all children to give an answer eventually.

Never immediately tell them they are wrong. Always help them with the right answer.

If losing attention, either move on or switch to a silly question and then switch back.

IMPORTANT: Beforehand, teach the kids how to salute using the popcorn analogy so it does not appear too militaristic. Remind them frequently to use the salute — it helps build discipline. Be patient with their answers, let them speak, but try to keep it in turns.
