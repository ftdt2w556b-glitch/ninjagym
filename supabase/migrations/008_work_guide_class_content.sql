LIGHT STRETCH — Head to Toe (OPTIONAL)

PART 1 — Standing:
• Head to the right, left, forward and back with resistance
• Rotate shoulders forward, then backward
• Rotate arms both forward and back, in big and/or small circles
• Triceps stretch: pull the arms together behind the back
• Hands on the hips, bend the waist right, forward, left and then back
• Knee and ankle rotation — palms above kneecap. This stimulates a pressure point causing the legs to relax. Rotate knees and ankles right, left, inside and out.

NOTE: During this time you should be wearing a BIG smile. If you decide to do this stretching, don't take too long. Keep it exciting and beware of burning pauses. Switch appropriately. Remember to breathe (Silly).

PART 2 — Floor Stretches:
• Drop into the side-splits quickly and sit down saying, "Have a seat!" — Do this all quickly, keep from having a boring pause. This is comic relief as most can't do it.
• Stretch with the legs apart, to the right, center and left
• Left leg in and stretch toward the right foot. Repeat on left.
• 'Butterfly' stretch: holding the feet in and pushing downward on the knees with the forearms and/or elbows
• Arms out, stand up without using your hands, saying, "Have a stand!" Or "Okay, Jump up real fast!"

BREATHING FOCUS: In through the nose and out through the mouth. Both hands in the direction of the stretch. Heads up, back straight — breathe, but breathe silly. In through the nose before entering a stretch and out through the mouth when changing or lowering into a stretch.
